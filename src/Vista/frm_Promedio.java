    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (val_Empty()) {/// si val_empty es == true ejecuta el codigo
            ///instanciamos el objeto alumno q lo declaramos al inicio de la clase para poder utilizarlo en cualquier momento
            Alumno alm = new Alumno();
            //llenamos el objeto con los datos a traves de los metodos set de la clase alumno
            alm.setMatricula(txtMatricula.getText());
            alm.setNombre(txtNombre.getText());
            alm.setApellidoP(txtApellidoP.getText());
            alm.setApellidoM(txtApellidoM.getText());
            alm.setPromedio(cboPromedio.getSelectedIndex());
            control.guardar(alm);/// llamamos al metodo guardar del controlador
            control.limpiar(pDatos, 1);/// llamamos al metodo limpiar del controlador, mandamos como parametro el nombre del panel y 1 para txt y 2 para lbl
            cboPromedio.setSelectedIndex(0);
            txtMatricula.requestFocus();
            lblcantAlumnos.setText(String.valueOf(control.mostrar().size()));///nos muestra la cantidad de alumnos q tenemos registrados
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

