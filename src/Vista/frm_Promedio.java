    private void BtnMostarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnMostarActionPerformed
        int reg = control.mostrar().size();
        if (reg == 0) {
            JOptionPane.showMessageDialog(null, "No hay alumnos registrados");
        } else {
            DefaultTableModel m = tabla();
            Object[] columna = new Object[5];
            int media = 0, acumuladorProm = 0;
            int max = 0, indice = 0;
            for (int i = 0; i < control.mostrar().size(); i++) {

                columna[0] = control.mostrar().get(i).getMatricula();
                columna[1] = control.mostrar().get(i).getNombre();
                columna[2] = control.mostrar().get(i).getApellidoP();
                columna[3] = control.mostrar().get(i).getApellidoM();
                int promedio = control.mostrar().get(i).getPromedio();
                columna[4] = promedio;
                m.addRow(columna);
                acumuladorProm += promedio;// sumamos todos los promedio
                if (max < promedio) {//evaluamos para saber cual es el promedio mayor
                    max = promedio;
                    indice = i;///guardamos el valor del indice del promedio mayor para obtener los datos
                }
            }

            media = Math.round(acumuladorProm / reg);///calculamos la media
            String primerP = control.mostrar().get(indice).getNombre() + " " + control.mostrar().get(indice).getApellidoP() + " " + control.mostrar().get(indice).getApellidoP();
            tblDatos.setModel(m);//// se le entrega el modelo con todas las columnas a la tabla
            lblPrimerP.setText(primerP);
            lblPromedio.setText(String.valueOf(max));
            lblMedia.setText(String.valueOf(media));
        }
    }//GEN-LAST:event_BtnMostarActionPerformed

    private boolean val_Empty() {
        boolean v = false;
        if (txtMatricula.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un número de matricula");
            txtMatricula.requestFocus();
        } else if (txtNombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un nombre");
            txtNombre.requestFocus();
        } else if (txtApellidoP.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el apellido paterno");
            txtApellidoP.requestFocus();
        } else if (txtApellidoM.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese el apellido materno");
            txtApellidoM.requestFocus();
        } else if (cboPromedio.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione una nota de la lista");
            cboPromedio.requestFocus();
        } else {
            v = true;
        }
        return v;
    }

